#include "libft.h"

int		ft_isalnum(int c)
{
	if ((c >= (int)'0' && c <= (int)'9') || (c >= (int)'a' && c <= (int)'z') ||
		(c >= (int)'A' && c <= (int)'Z'))
		return (1);
	return (0);
}
